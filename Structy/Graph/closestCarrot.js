const grid = [
  ['O', 'O', 'X', 'C', 'O'],
  ['O', 'X', 'X', 'X', 'O'],
  ['C', 'X', 'O', 'O', 'O'],
];

closestCarrot(grid, 2, 2); // -> 5

//iterative breadth first
//r = number of rows
//c = number of columns
// time O(rc ^ 2), space O(rc)
const closestCarrot = (grid, startRow, startCol) => {
    const visited = new Set([startRow + ',' + startCol])
    const queue = [[startRow, startCol, 0]]
    while(queue.length > 0){
      const [row, column, distance] = queue.shift()
      
      if(grid[row][column] === 'C') {
       return distance
      }
      
      const deltas = [[0, 1],[0, -1],[1, 0],[-1, 0]]
      
      for(let delta of deltas){
        const [rowDelta, columnDelta] = delta
        const neighborRow = row + rowDelta
        const neighborColumn = column + columnDelta
        
        const rowInBound = 0 <= neighborRow && neighborRow < grid.length
        const columnInBound = 0 <= neighborColumn && neighborColumn < grid[0].length
        const neighborPosition = neighborRow + ',' + neighborColumn
        if(rowInBound && columnInBound && grid[neighborRow][neighborColumn] !== 'X' && !visited.has(neighborPosition)){
          queue.push([neighborRow, neighborColumn, distance + 1])
          visited.add(neighborPosition)
        }
      }
      } 
    return -1
  }
  
